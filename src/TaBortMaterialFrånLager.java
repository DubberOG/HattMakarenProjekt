    private void btnRaderaMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRaderaMaterialActionPerformed
        try {

            String valdMaterial = (String) cbValjProdukt.getSelectedItem();
            String valdMaterialID = valdMaterial.split(" - ")[0]; // Hämta ProduktID från kombinerad sträng
            String nyTaBortFraga = "DELETE FROM MaterialLager  WHERE MaterialID = '"+valdMaterialID+"'";
            Main.idb.delete(nyTaBortFraga);
            JOptionPane.showMessageDialog(null, "Materialet togs bort från lager!");

        } catch (InfException e){
            System.out.println ("internt felmedelande:" + e.getMessage());
        }
    }//GEN-LAST:event_btnRaderaMaterialActionPerformed
private void fyllCbValjProdukt() {
            try{
        ArrayList<HashMap<String, String>> allaProdukter = Main.idb.fetchRows("SELECT MaterialID, Namn FROM MaterialLager");
                  
                for(HashMap<String, String> produkt : allaProdukter){
                // Hämta KundID och Namn från HashMap
            String produktID = produkt.get("MaterialID");
            String namn = produkt.get("Namn");
            
            // Skapa en sträng som innehåller både KundID och namn och lägg till i comboboxen
            cbValjProdukt.addItem(produktID + " - " + namn);  
           
                }}
                    
              
        catch(InfException ettUndantag){
            JOptionPane.showMessageDialog(null, "Databasfel!");
         }}
