    private void btnSokKnappActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSokKnappActionPerformed
        
         ArrayList<String> lagerMaterialStatus = new ArrayList<>();
        try{
            String nyLagerMaterialFraga = "SELECT Namn, Antal FROM MaterialLager";
            ArrayList<HashMap<String,String>> materialLagrade = Main.idb.fetchRows(nyLagerMaterialFraga);
            for (HashMap<String,String> materialLager : materialLagrade) {
                for (String MaterialLager : materialLager.keySet()) {
            lagerMaterialStatus.add(MaterialLager + ": " + materialLager.get(MaterialLager));
        }
    }
           for (String materialLager : lagerMaterialStatus) {
            txtAreaRuta.append(materialLager + "\n");
    }

        }catch (InfException e){
            System.out.println ("internt felmedelande:" + e.getMessage());
        }
    }//GEN-LAST:event_btnSokKnappActionPerformed

