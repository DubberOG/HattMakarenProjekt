         private void fyllCbValjHattmakare() {
            try{
        ArrayList<HashMap<String, String>> allaHattmakare = Main.idb.fetchRows("SELECT MedarbetarID, Namn FROM Medarbetare");
                  
                for(HashMap<String, String> hattmakare : allaHattmakare){
                // Hämta KundID och Namn från HashMap
            String medarbetarID = hattmakare.get("MedarbetarID");
            String namn = hattmakare.get("Namn");
            
            // Skapa en sträng som innehåller både KundID och namn och lägg till i comboboxen
            cbValjHattmakare.addItem(medarbetarID + " - " + namn);    
           
                }}
                    
              
        catch(InfException ettUndantag){
            JOptionPane.showMessageDialog(null, "Databasfel!");
         }}
