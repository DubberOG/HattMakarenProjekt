    private void btnUppdateraTimprisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUppdateraTimprisActionPerformed

        try {
            if (Validering.txtFaltTomt(txtAngeTimpris)){

                String strTimpris = txtAngeTimpris.getText();
                double angeTimpris = Double.parseDouble(strTimpris);
                String hattmakare = cbValjHattmakare.getSelectedItem().toString();
                String valdHattmakarID = hattmakare.split(" - ")[0];

                String uppdateraTimprisHattmakare = "UPDATE Medarbetare SET Timpris = '" + angeTimpris + "' WHERE MedarbetarID = '" + valdHattmakarID + "'";
                Main.idb.update(uppdateraTimprisHattmakare);
  

                JOptionPane.showMessageDialog(null, "Timpriset har uppdaterats!");

            }}
            catch  (InfException e){
                System.out.println ("internt felmedelande:" + e.getMessage());
            }
    }//GEN-LAST:event_btnUppdateraTimprisActionPerformed

