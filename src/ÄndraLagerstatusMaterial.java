    public void väljMaterial()
    {
        try{
             ArrayList<HashMap<String, String>> allaMaterial = Main.idb.fetchRows("SELECT Namn FROM MaterialLAger");
             for(HashMap<String, String> ettMaterial : allaMaterial)
             {
                String namnMaterial = ettMaterial.get("Namn");
             cbVäljMaterial.addItem(namnMaterial); 
             }
           
        }
        catch(InfException e)
        {
          JOptionPane.showMessageDialog(null, "Error");
        }
    }
    
    public void uppdatera()
    {
        String valtÅtgärd = cbVäljÅtgärd.getSelectedItem().toString();
        int angeAntal = Integer.parseInt(txtAnge.getText());
        if(valtÅtgärd.equals("Lägg till"))
            
        {
            try
            {
               if(angeAntal > 0)
               {
                 Main.idb.update("Update MaterialLager set Antal = antal + " + angeAntal + " where Namn ='" + cbVäljMaterial.getSelectedItem() + "'");
               }
            }
            catch(InfException e)
            {
                JOptionPane.showMessageDialog(null, "Error");
            }
            
        }
        
        else if (valtÅtgärd.equals("Ta bort"))
            
        {
            try
            {
               if(angeAntal > 0)
               {
                 Main.idb.update("Update MaterialLager set Antal = antal - " + angeAntal + " where Namn ='" + cbVäljMaterial.getSelectedItem() + "'");
               }
            }
            catch(InfException e)
            {
                JOptionPane.showMessageDialog(null, "Error");
            }
    }
    }
